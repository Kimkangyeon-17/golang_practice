package main

import "fmt"

func main() {

	day := 3

	if day == 1 {
		fmt.Println("첫째 날입니다.")
	} else if day == 2 {
		fmt.Println("둘째 날입니다.")
	} else if day == 3 {
		fmt.Println("셋째 날입니다.")
	} else if day == 4 {
		fmt.Println("넷째 날입니다.")
	} else if day == 5 {
		fmt.Println("다섯째 날입니다.")
	} else {
		fmt.Println("최종일입니다.")
	}
}